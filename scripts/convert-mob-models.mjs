// Convert Blockbench/GeckoLib .bbmodel files (box_uv animated entity models)
// plus their PNG textures into the game's baked model JSON. Usage:
//   node game/scripts/convert-bbmodels.mjs <packDir>
// where <packDir> contains models/*.bbmodel and textures/*.png.
// Output: game/src/content/dragons-data.ts
//
// Kept per model: element cubes (from/to/origin/rotation/uv), the bone tree
// (pivots + rotations, cube membership), every animation's per-bone
// rotation/position keyframes, and the texture as a data URI.

import { readdirSync, readFileSync, writeFileSync } from "fs";
import { join, dirname, basename } from "path";
import { fileURLToPath } from "url";

const packDirs = process.argv.slice(2);
if (packDirs.length === 0) {
  console.error("usage: node convert-bbmodels.mjs <packDir> [morePackDirs...]");
  process.exit(1);
}

const models = [];
const jobs = [];
for (const packDir of packDirs) {
  const modelsDir = join(packDir, "models");
  const texturesDir = join(packDir, "textures");
  for (const file of readdirSync(modelsDir).filter((f) => f.endsWith(".bbmodel")).sort()) {
    jobs.push({ modelsDir, texturesDir, file });
  }
}

for (const { modelsDir, texturesDir, file } of jobs) {
  const d = JSON.parse(readFileSync(join(modelsDir, file), "utf8"));
  const id = basename(file, ".bbmodel");
  const res = d.resolution ?? { width: 64, height: 64 };

  // Elements: box-UV cubes carry uv_offset; "free"-format cubes carry
  // explicit per-face UV rects instead.
  const elements = {};
  for (const e of d.elements ?? []) {
    if (e.type && e.type !== "cube") continue;
    // Collision helpers are cubes in some packs, groups in others.
    if (/hit_?box/i.test(e.name ?? "")) continue;
    const el = {
      f: e.from,
      t: e.to,
      o: e.origin ?? [0, 0, 0],
      r: e.rotation ?? null,
      inf: e.inflate ?? 0,
    };
    if (e.uv_offset) el.uv = e.uv_offset;
    else if (e.faces) {
      const fuv = {};
      for (const [face, def] of Object.entries(e.faces)) {
        if (def?.uv) fuv[face] = def.uv;
      }
      el.fuv = fuv;
    } else el.uv = [0, 0];
    elements[e.uuid] = el;
  }

  // Bone tree from the outliner; hitbox helpers are skipped.
  const walk = (node) => {
    if (typeof node === "string") return null;
    if (/hitbox/i.test(node.name)) return null;
    const bone = {
      n: node.name,
      o: node.origin ?? [0, 0, 0],
      r: node.rotation ?? null,
      cubes: [],
      kids: [],
    };
    for (const child of node.children ?? []) {
      if (typeof child === "string") {
        if (elements[child]) bone.cubes.push(child);
      } else {
        const kid = walk(child);
        if (kid) bone.kids.push(kid);
      }
    }
    return bone;
  };
  const roots = (d.outliner ?? []).map(walk).filter(Boolean);

  // Animations: per-bone rotation/position keyframe tracks.
  const anims = [];
  for (const a of d.animations ?? []) {
    const bones = {};
    for (const animator of Object.values(a.animators ?? {})) {
      if (!animator.name || !Array.isArray(animator.keyframes)) continue;
      const rot = [];
      const pos = [];
      for (const kf of animator.keyframes) {
        const p = kf.data_points?.[0];
        if (!p) continue;
        const v = [Number(kf.time) || 0, parseFloat(p.x) || 0, parseFloat(p.y) || 0, parseFloat(p.z) || 0];
        if (kf.channel === "rotation") rot.push(v);
        else if (kf.channel === "position") pos.push(v);
      }
      rot.sort((x, y) => x[0] - y[0]);
      pos.sort((x, y) => x[0] - y[0]);
      if (rot.length || pos.length) bones[animator.name] = { rot, pos };
    }
    anims.push({ name: a.name, len: Number(a.length) || 1, loop: a.loop === "loop", bones });
  }

  // Texture: a provided skin in the textures dir wins (we pair every model
  // with its Faithful entity skin), then the model's embedded data URI, then
  // its named file. Faithful skins share the vanilla UV layout these models
  // are authored against, so they map correctly.
  const t0 = d.textures?.[0];
  let tex = null;
  for (const cand of [`${id}.png`, id, t0?.name]) {
    if (!cand) continue;
    try {
      tex = `data:image/png;base64,${readFileSync(join(texturesDir, cand)).toString("base64")}`;
      break;
    } catch { /* try next */ }
  }
  if (!tex && t0?.source?.startsWith("data:image/")) tex = t0.source;
  if (!tex) console.warn(`  ${id}: no texture found`);

  models.push({ id, resW: res.width, resH: res.height, elements, roots, anims, tex });
  console.log(`${id}: ${Object.keys(elements).length} cubes, ${anims.map((a) => a.name).join("/")}`);
}

const json = JSON.stringify({ version: 1, models });
const __dir = dirname(fileURLToPath(import.meta.url));
writeFileSync(
  join(__dir, "../src/content/mob-models-data.ts"),
  "// Generated by game/scripts/convert-mob-models.mjs — do not edit.\n" +
    "// Baked from the BetaSharp mob models + Faithful entity skins.\n" +
    `export const MOBS_JSON: string = ${JSON.stringify(json)};\n`,
);
console.log(`-> game/src/content/mob-models-data.ts (${(json.length / 1024).toFixed(0)} KB)`);
