// Merge a single .bbmodel (+ texture) into the already-baked MOBS_JSON,
// replacing any model with the same id, WITHOUT regenerating the whole file
// (the other models' source packs are no longer in the tree). Usage:
//   node game/scripts/merge-mob-model.mjs <packDir> [morePackDirs...]
// where <packDir> holds models/*.bbmodel and textures/*.png. Same element/
// bone/anim/texture extraction as convert-mob-models.mjs — kept in sync.

import { readdirSync, readFileSync, writeFileSync } from "fs";
import { join, dirname, basename } from "path";
import { fileURLToPath } from "url";

const packDirs = process.argv.slice(2);
if (packDirs.length === 0) {
  console.error("usage: node merge-mob-model.mjs <packDir> [morePackDirs...]");
  process.exit(1);
}

function convert(modelsDir, texturesDir, file) {
  const d = JSON.parse(readFileSync(join(modelsDir, file), "utf8"));
  const id = basename(file, ".bbmodel");
  const res = d.resolution ?? { width: 64, height: 64 };

  const elements = {};
  for (const e of d.elements ?? []) {
    if (e.type && e.type !== "cube") continue;
    if (/hit_?box/i.test(e.name ?? "")) continue;
    const el = { f: e.from, t: e.to, o: e.origin ?? [0, 0, 0], r: e.rotation ?? null, inf: e.inflate ?? 0 };
    if (e.uv_offset) el.uv = e.uv_offset;
    else if (e.faces) {
      const fuv = {};
      for (const [face, def] of Object.entries(e.faces)) if (def?.uv) fuv[face] = def.uv;
      el.fuv = fuv;
    } else el.uv = [0, 0];
    elements[e.uuid] = el;
  }

  const walk = (node) => {
    if (typeof node === "string") return null;
    if (/hitbox/i.test(node.name)) return null;
    const bone = { n: node.name, o: node.origin ?? [0, 0, 0], r: node.rotation ?? null, cubes: [], kids: [] };
    for (const child of node.children ?? []) {
      if (typeof child === "string") {
        if (elements[child]) bone.cubes.push(child);
      } else {
        const kid = walk(child);
        if (kid) bone.kids.push(kid);
      }
    }
    return bone;
  };
  const roots = (d.outliner ?? []).map(walk).filter(Boolean);

  const anims = [];
  for (const a of d.animations ?? []) {
    const bones = {};
    for (const animator of Object.values(a.animators ?? {})) {
      if (!animator.name || !Array.isArray(animator.keyframes)) continue;
      const rot = [];
      const pos = [];
      for (const kf of animator.keyframes) {
        const p = kf.data_points?.[0];
        if (!p) continue;
        const v = [Number(kf.time) || 0, parseFloat(p.x) || 0, parseFloat(p.y) || 0, parseFloat(p.z) || 0];
        if (kf.channel === "rotation") rot.push(v);
        else if (kf.channel === "position") pos.push(v);
      }
      rot.sort((x, y) => x[0] - y[0]);
      pos.sort((x, y) => x[0] - y[0]);
      if (rot.length || pos.length) bones[animator.name] = { rot, pos };
    }
    anims.push({ name: a.name, len: Number(a.length) || 1, loop: a.loop === "loop", bones });
  }

  const t0 = d.textures?.[0];
  let tex = null;
  for (const cand of [`${id}.png`, id, t0?.name]) {
    if (!cand) continue;
    try {
      tex = `data:image/png;base64,${readFileSync(join(texturesDir, cand)).toString("base64")}`;
      break;
    } catch { /* try next */ }
  }
  if (!tex && t0?.source?.startsWith("data:image/")) tex = t0.source;
  if (!tex) console.warn(`  ${id}: no texture found`);

  return { id, resW: res.width, resH: res.height, elements, roots, anims, tex };
}

const __dir = dirname(fileURLToPath(import.meta.url));
const outPath = join(__dir, "../src/content/mob-models-data.ts");
const existing = readFileSync(outPath, "utf8");
const jsonStr = existing.match(/MOBS_JSON: string = ("(?:[^"\\]|\\.)*")/s)[1];
const data = JSON.parse(JSON.parse(jsonStr));
const byId = new Map(data.models.map((m) => [m.id, m]));

for (const packDir of packDirs) {
  const modelsDir = join(packDir, "models");
  const texturesDir = join(packDir, "textures");
  for (const file of readdirSync(modelsDir).filter((f) => f.endsWith(".bbmodel")).sort()) {
    const m = convert(modelsDir, texturesDir, file);
    const verb = byId.has(m.id) ? "replaced" : "added";
    byId.set(m.id, m);
    const named = new Set();
    const collect = (b) => { if (b.n) named.add(b.n); (b.kids ?? []).forEach(collect); };
    m.roots.forEach(collect);
    console.log(`${verb} ${m.id}: ${Object.keys(m.elements).length} cubes, bones[${[...named].join(",")}]`);
  }
}

data.models = [...byId.values()].sort((a, b) => a.id.localeCompare(b.id));
const json = JSON.stringify(data);
writeFileSync(
  outPath,
  "// Generated by game/scripts/convert-mob-models.mjs — do not edit.\n" +
    "// Baked from the BetaSharp mob models + Faithful entity skins.\n" +
    `export const MOBS_JSON: string = ${JSON.stringify(json)};\n`,
);
console.log(`-> src/content/mob-models-data.ts (${(json.length / 1024).toFixed(0)} KB, ${data.models.length} models)`);
