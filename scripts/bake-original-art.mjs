import { readdir, readFile, writeFile } from "node:fs/promises";
import { basename, dirname, join, relative, resolve } from "node:path";
import { fileURLToPath } from "node:url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const artRoot = join(root, "src", "render", "art");
const outFile = join(root, "src", "render", "original-art.ts");

async function pngs(dir) {
  const names = await readdir(dir);
  return names.filter((name) => name.endsWith(".png")).sort();
}

async function encodeMap(entries) {
  const lines = [];
  for (const [key, file] of entries) {
    const b64 = (await readFile(file)).toString("base64");
    lines.push(`  ${JSON.stringify(key)}: ${JSON.stringify(b64)},`);
  }
  return lines.join("\n");
}

const textureEntries = [];
for (const folder of ["items", "runes"]) {
  const dir = join(artRoot, folder);
  for (const name of await pngs(dir)) {
    const slug = basename(name, ".png").replace(/-64$/, "");
    textureEntries.push([`icon.original.${slug}`, join(dir, name)]);
  }
}

// ImageGen-authored material masters are deterministically reduced to exact
// runtime tiles under materials/tiles. Their dotted filename is the material
// key itself, e.g. terrain.grass.top-64.png -> terrain.grass.top.
const materialTileDir = join(artRoot, "materials", "tiles");
for (const name of await pngs(materialTileDir)) {
  const key = basename(name, ".png").replace(/-64$/, "");
  textureEntries.push([key, join(materialTileDir, name)]);
}

const entityDir = join(artRoot, "entities");
const entityEntries = (await pngs(entityDir)).map((name) => [
  `entity.${basename(name, ".png")}`,
  join(entityDir, name),
]);
const sheepFile = join(entityDir, "sheep.png");
entityEntries.push(["entity.sheep.skin", sheepFile], ["entity.sheep.wool", sheepFile]);

const source = `// Generated by scripts/bake-original-art.mjs from src/render/art.
// These original assets override bundled placeholder-pack art. User-imported
// texture packs still take precedence in MaterialResolver.

export const ORIGINAL_TEXTURES: Record<string, string> = {
${await encodeMap(textureEntries)}
};

export const ORIGINAL_ENTITY_TEXTURES: Record<string, string> = {
${await encodeMap(entityEntries)}
};
`;

await writeFile(outFile, source);
console.log(`Baked ${textureEntries.length} original textures and ${entityEntries.length} entity skins to ${relative(root, outFile)}.`);
